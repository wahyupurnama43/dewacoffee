                                        How often do you drink coffee a day? 2 times? 3 times? For me, usually 2 times a day. Then how much coffee grounds are used up when you brew coffee? 30 grams? 45 grams? If you drink coffee 2 times a
                                        day, and drink coffee once using 15 grams of coffee, then the total coffee you produce in a day is 30 grams. Have you ever thought about using coffee grounds for something other than just ending up in
                                        the trash? This time, Kopitem wants to provide tips on how to use coffee grounds in everyday life. Here are 5 tips for using coffee grounds in everyday life: 1. Eliminate Bad Odors One of the benefits
                                        of coffee grounds is to get rid of bad odors in the house. Well, usually the refrigerator freezer is often a place that has a bad smell. Then there is nothing wrong with using coffee grounds to get
                                        rid of the unpleasant smell. You can put coffee grounds in a container that is resistant to cold temperatures. The amount of coffee grounds that can be tailored to your needs. Usually the odor will
                                        disappear in less than a day. Apart from being in the fridge freezer, the bathroom is also a place that sometimes causes unpleasant odors. You can prepare a cloth then fill it with enough coffee, and
                                        hang it in the bathroom. Voila, the bad smell in the bathroom will disappear. 2. Cleaning the dust in the house Have you ever had trouble sweeping the dust in the house, because the dust often flies?
                                        Well, coffee grounds can help you. It turns out that dried coffee grounds can help us keep the dust from flying. The method is also quite easy, sprinkle dried coffee grounds in dusty corners of the
                                        house, then the dust will be easier to sweep because it won't fly. Let's try it. 3. Get rid of ants The third benefit of coffee grounds is that they can be used to kill ants. Do not believe? Ants
                                        often appear on the side of the kitchen or on the sidelines of dining tables and kitchen utensils due to food residue. Instead of using an insect cleaner that contains chemicals, why not use coffee
                                        grounds? The trick is to simply sprinkle coffee grounds on the side or between the kitchen and dining table where ants are frequently visited, so the ants will be "lazy" to come again. Apparently not
                                        including the smell of coffee which is quite pungent. Easy enough, right? 4. Eliminating Scratches on Wood Have a wooden household furniture but it's been scratched a lot both on purpose and
                                        accidentally? Try removing the scratch using coffee grounds. Yep, the benefits of coffee grounds further remove scratches on wood. How to? Use the coffee grounds left over from drinking coffee in the
                                        morning, prepare a cotton bud as a tool for applying coffee grounds to wooden furniture. Put coffee grounds on the scratched part of the wooden furniture, then leave it for about 10 minutes, then
                                        clean the coffee grounds with a clean cloth. And look at the result, are the scratches gone? If the scratches haven't disappeared, you can adjust the previous step once again. Keep in mind that the
                                        scratches that this method can remove are ones that aren't very deep. 5. Cleaning Kitchen Appliances The fifth benefit of coffee grounds is that they can be used to clean kitchen utensils. Have you
                                        ever had difficulty cleaning stains on kitchen utensils in your home? There is nothing wrong with you trying to use coffee grounds to clean it. How to? You can rub a dishwashing sponge that has been
                                        spiked with coffee grounds onto the stubborn stain. The slightly rough texture on the coffee grounds will help clean the stain.
